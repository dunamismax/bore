    // -----------------------------------------------------------------------
    // Exhaustive transition matrix test
    // -----------------------------------------------------------------------

    #[test]
    fn exhaustive_transition_matrix() {
        use SessionState::*;
        let all = [Created, Waiting, Connected, Transferring, Complete, Failed];

        // Define the expected outcomes: (from, to) -> should succeed
        let expected_ok: &[(SessionState, SessionState)] = &[
            (Created, Waiting),
            (Created, Failed),
            (Waiting, Connected),
            (Waiting, Failed),
            (Connected, Transferring),
            (Connected, Failed),
            (Transferring, Complete),
            (Transferring, Failed),
        ];

        for &from in &all {
            for &to in &all {
                let result = from.validate_transition(to);
                let should_be_ok = expected_ok.contains(&(from, to));
                assert_eq!(
                    result.is_ok(),
                    should_be_ok,
                    "transition {from} -> {to}: expected ok={should_be_ok}, got ok={}",
                    result.is_ok()
                );
            }
        }
    }
